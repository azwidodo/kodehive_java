package com.fundamental.day2;

public class ConditionalIfElse {

	public static void main(String[] args) {
		
		String kondisi = "Bekerja";
		
		if (kondisi.toLowerCase().equals("bekerja")) {
			System.out.println("Usia 21 ke atas");
		} else {
			System.out.println("Usia di bawah 21");
		}
		
	}

}
